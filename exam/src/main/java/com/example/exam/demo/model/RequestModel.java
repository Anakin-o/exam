package com.example.exam.demo.model;

public class RequestModel {
    private String chemData;
    private String chemDataType;
    private String height;
    private String imageType;
    private String resolution;

    public String getChemData() {
        return chemData;
    }

    public void setChemData(String chemData) {
        this.chemData = chemData;
    }

    public String getChemDataType() {
        return chemDataType;
    }

    public void setChemDataType(String chemDataType) {
        this.chemDataType = chemDataType;
    }

    public String getHeight() {
        return height;
    }

    public void setHeight(String height) {
        this.height = height;
    }

    public String getImageType() {
        return imageType;
    }

    public void setImageType(String imageType) {
        this.imageType = imageType;
    }

    public String getResolution() {
        return resolution;
    }

    public void setResolution(String resolution) {
        this.resolution = resolution;
    }

    public String getWidth() {
        return width;
    }

    public void setWidth(String width) {
        this.width = width;
    }

    private String width;
}
